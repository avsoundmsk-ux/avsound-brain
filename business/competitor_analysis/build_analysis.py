def prox(km):
    if km is None: return 3
    if km <= 2.0: return 5
    if km <= 4.0: return 4
    return 3

rows = []
for r in D:
    name, cat, addr, km, rating, rev, serv, price, rel, note = r
    if cat is None:  # skip guard placeholder
        continue
    rating = rating or 0.0; rev = rev or 0; serv = serv or 0
    score = rating*2 + math.log10(rev+1)*1.5 + serv*1 + prox(km)*1
    rows.append({
        "name":name,"category":cat,"address":addr,"dist_km_est":km,
        "rating":rating,"reviews":rev,"services_0_7":serv,
        "sample_price":price,"strength_score":round(score,2),
        "relevance":rel,"note":note,"source":"Яндекс.Карты"
    })

rows.sort(key=lambda x:-x["strength_score"])

cols = ["name","category","address","dist_km_est","rating","reviews","services_0_7","sample_price","strength_score","relevance","note","source"]
